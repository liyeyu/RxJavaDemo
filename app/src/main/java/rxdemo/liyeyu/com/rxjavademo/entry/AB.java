public class AB {
    public int a;
    public int b;

    public AB(int a, int b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "AB{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
